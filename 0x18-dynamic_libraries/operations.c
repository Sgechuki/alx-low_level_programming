#include "op.h"

/**
 * add - performs addition on two integers
 * @a: integer
 * @b: integer
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - perform substraction on two integers
 * @a: integer
 * @b: integer
 * Return: diffrence of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies a and b
 * @a: integer
 * @b: integer
 * Return: product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides a by b
 * @a: integer
 * @b: integer
 * Return: division of a and b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - finds modulus of a dived by b
 * @a: integer
 * @b: integer
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
